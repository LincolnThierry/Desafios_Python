# Comparação de Numeros Inteiros

a = int(input(print('Digita um numero inteiro:')).strip())
b = int(input(print('Digite um numeto inteiro:')).strip())

if a > b:
    print('O primeiro valor é Maior')
elif b > a:
    print('O segundo valor é Maior')
elif a and b == a and b:
    print('Não existe valor maior, os dois são iguais A ={} e B ={}'.format(a,b).title())
